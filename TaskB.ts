/** Question 119
Create a while loop that logs `Hello, World!` 5 times.*/

//-------------------------------------------------------

// Initialize a counter variable
let count = 0;

// Loop while the counter is less than 5
while (count < 5) {
  // Log 'Hello, World!' to the console
  console.log("Hello, World!");

  // Increment the counter
  count++;
}
